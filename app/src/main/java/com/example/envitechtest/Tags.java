package com.example.envitechtest;

public class Tags {

    public String Label;
    public String Color;

    public Tags() {
    }

    public Tags(String label, String color) {
        Label = label;
        Color = color;
    }

    public String getLabel() {
        return Label;
    }

    public void setLabel(String label) {
        Label = label;
    }

    public String getColor() {
        return Color;
    }

    public void setColor(String color) {
        Color = color;
    }
}
